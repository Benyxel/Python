def square(x):
    return x * x
result = square(5)
print(result)


def mul3(x):
    return x * 3
result = mul3(3)
print(result)


def sub3(x):
    return x - 3
result = sub3(3)
print(result)


def add(x, b):
    return x + b
result = add(3, 5)
print(result)


def prod(x, y):
    return x * y
result = prod(3, 5)
print(result)


def product(x, y, z):
    return x * y * z
result = product(3, 5, 7)
print(result)


def gt3(x):
    return x > 3
result = gt3(1)
print(result)


def lt10(x):
    return x < 10
result = lt10(11)
print(result)


def both_gt3(x, y):
    return x > 3 and y > 3
result = both_gt3(4, 4)
print(result)


def sum3(list):
    return sum3(list)
result = sum3([1, 2, 3])
